#include "print_menu.h"

void print_menu() {
		std::cout << "1: Enter 1 to double the number." << std::endl;
		std::cout << "2: Enter 2 to reverse the digits of the number." << std::endl;
		std::cout << "3: Enter 3 to raise the number to the power of the second digit." << std::endl;
		std::cout << "4: Enter 4 to sum the digits of the number." << std::endl;
		std::cout << "5: Enter 5 to raise the first digit to the power of the second digit." << std::endl;
		std::cout << "6: Enter 6 to raise the first two digits the power of the last digit." << std::endl;
		std::cout << "9: Enter 9 to terminate the program." << std::endl;
}
